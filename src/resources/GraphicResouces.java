	private static PImage[] byteToImage(byte[] bytes, int tileWidth, int tileHeight) {
	    try {
	    	ByteArrayInputStream bais = new ByteArrayInputStream(bytes);
	    	BufferedImage source = ImageIO.read(bais);
	    	
	    	int line = source.getHeight() / tileHeight;
	    	int column = source.getWidth() / tileWidth;
	    	PImage[] output = new PImage[line * column];
	    	
			for(int l=0;l<line;l++) {
				int cl = l*column;
				for(int c=0;c<column;c++) {
					output[c+cl] = new PImage(source.getSubimage(c * tileWidth, l * tileHeight, tileWidth, tileHeight));
				}
			}
			return output;
	    }catch (Exception e) {
	        throw new RuntimeException(e);
	    }
	}
	
